package employee;

public class Employee {
	public Integer id;
	public String nama;
	public String location;
	public String tipeResto;
	public Employee(Integer id, String nama, String location, String tipeResto) {
		super();
		this.id = id;
		this.nama = nama;
		this.location = location;
		this.tipeResto = tipeResto;
	}
	
}
